
public class Rango {

    // Atrivutos
    private int max;
    private int min;

    // Constuctor
    public Rango() {

    }

    public Rango(int min, int max) {
        this.min = min;
        this.max = max;
    }

    // Getters y setters
    public int getMax() {
        return max;
    }

    public void setMax(int max) {
        this.max = max;
    }

    public int getMin() {
        return min;
    }

    public void setMin(int min) {
        this.min = min;
    }

}
